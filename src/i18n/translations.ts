export const t = (key: string, language: SupportedLanguage): string => {
  const translation = translations[language];
  if (translation && key in translation) {
    return translation[key as keyof typeof translation];
  }
  // Fallback to English
  const englishTranslation = translations['en'];
  return englishTranslation[key as keyof typeof englishTranslation] || key;
};
